        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;รายงานถือเป็นส่วนหนึ่งของการปฏิบัติงานสหกิจศึกษา   มีวัตถุประสงค์เพื่อฝึกฝนทักษะการสื่อสาร(Communication Skill) ของนิสิต และจัดทำข้อมูลที่เป็นประโยชน์สำหรับสถานประกอบการ นิสิตจะต้อง
ขอรับคำปรึกษาจากพนักงานที่ปรึกษา (JobSupervisor) เพื่อกำหนดหัวข้อรายงานที่เหมาะสมโดยคำนึงถึงความต้องการของสถานประกอบการเป็นหลัก ตัวอย่างของรายงานได้แก่ผลงานวิจัยที่นิสิตปฏิบัติ รายงาน
วิชาการที่น่าสนใจ การสรุปข้อมูลหรือสถิติบางประการ การวิเคราะห์และประเมินผลข้อมูล เป็นต้นทั้งนี้ รายงานอาจจะจัดทำเป็นกลุ่มของนิสิตสหกิจศึกษามากกว่า 1คนก็ได้<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ในกรณีที่สถานประกอบการไม่ต้องการรายงานในหัวข้อข้างต้น นิสิตจะต้องพิจารณาเรื่องที่ตนสนใจและหยิบยกมาทำรายงาน โดยปรึกษากับพนักงานที่ปรึกษาเสียก่อน ตัวอย่างหัวข้อที่จะใช้เขียนรายงาน 
ได้แก่ รายงานวิชาการที่นิสิตสนใจ รายงานการปฏิบัติงานที่ได้รับมอบหมาย หรือแผนและวิธีการปฏิบัติงานที่จะทำให้บรรลุถึงวัตถุประสงค์ของการเรียนรู้ที่นิสิตวางเป้าหมายไว้จากการปฏิบัติงานสหกิจศึกษาครั้งนี้ 
(Learning Objectives) เมื่อกำหนดหัวข้อได้แล้ว ให้นิสิตจัดทำโครงร่างของเนื้อหารายงานพอสังเขปตามแบบฟอร์มนี้ ทั้งนี้ให้ปรึกษากับ Job Supervisor เสียก่อนแล้วจึงส่งกลับมายังสหกิจศึกษา ภายในระยะเวลาที่กำหนด<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;คณะวิทยาการสารสนเทศ จะรวบรวมนำเสนออาจารย์ที่ปรึกษาสหกิจศึกษาเพื่อพิจารณาหากอาจารย์มีข้อเสนอแนะจะส่งกลับให้นิสิตทราบ และเพื่อมิให้เป็นการเสียเวลา นิสิตควรดำเนินการเขียนรายงานโดยทันที
